def get_lang():
    lang = session.get('lang', DEFAULT_LANG)
    return lang if lang in SUPPORTED_LANGS else DEFAULT_LANG


def t(text):
    """翻译文案：英文为 key，按当前语言返回译文，缺失回退原文。"""
    lang = get_lang()
    if lang == 'en':
        return text
    return TRANSLATIONS.get(lang, {}).get(text, text)
